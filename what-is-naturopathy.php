                     <h2><span>What is Naturopathy?</span></h2>
                     <p>Staying Healthy Naturopathy</p>
                  </header>
                  <p>Naturopathy is a system of healing that greatly stimulates and encourages the body's own capacity to heal itself, using the laws of nature. We would address the possible causes of an illness (for example, stress, poor diet and etc.) as well as the symptoms.
                     Disease may initially begin from: diet, lifestyle, an illness, stress, emotion, events, exposure to toxicity or genetic inheritance.<br><br>
                     Naturopathic medicine emphasizes health restoration and treatment of disease by using diet modification, nutritional supplements, herbal medicine, and lifestyle counselling.
                  </p>
                  <h3>The key principles in naturopathic medicine:</h3>
                  <p>
                  <li><span><b> Do No Harm</b></span></li>
                  Naturopathic medicine practices a natural, safe and effective treatment or intervention<br>
                  <b>The Naturopath's role?</b><br>
                  To provide natural, safe and the most effective treatment or intervention and minimise the risk of harmful side effects.<br><br>
                  <li><span><b> The Healing Power of Nature</b></span></li>
                  Naturopathic medicine believes that the body has an inherent self-healing capacity. It can prevent itself from diseases and regain optimum health. Nature is the greatest healer.<br>
                  <b>The Naturopath's role?</b><br>
                  To support, facilitate and encourage this process with the aid of natural therapies.<br>
                  To avoid suppression of symptoms whenever possible, as suppression of symptoms generally interferes with the healing process.<br>
                  To respect and work with the individual's self healing process in the treatment.<br><br>
                  <li><span><b> Identify and Treat the Cause</b></span></li>
                  Naturopathic medicine seeks to identify and remove the cause of illness.<br>
                  <b>The Naturopath's role?</b><br>
                  To identify the underlying causes of illness or disease, rather than to merely eliminate or suppress symptoms.<br><br>
                  <li><span><b> Practitioner as Teacher</b></span></li>
                  Naturopathic medicine considers the practitioner as the teacher who educates, inspires and motivates patients to take responsibility for their own health;<br>
                  <b>The Naturopath's role?</b><br>
                  To educate their patients and encourage self-responsibility for health, thereby, establish a good practitioner/patient relationship<br><br>
                  <li><span><b> Treat the Whole Person</b></span></li>
                  Naturopathic medicine treats the body as a whole and not just the disease by taking into account the physical, emotional, mental, spiritual, genetic, environmental, social factors and influences.<br>
                  <b>The Naturopath's role?</b><br>
                  To treat the whole body and not just the presenting symptoms or the disease<br><br>
                  <li><span><b> Prevention</b></span></li>
                  Naturopathic medicine emphasises the prevention of diseaseâ€”assessing risk factors, heredity and susceptibility to disease and making appropriate interventions.<br>
                  <b>The Naturopath's role?</b><br>
                  To emphasise the prevention of disease, making appropriate interventions in partnership with the patient, and to promote well-being.<br><br>
                  <b>What to expect from a visit to a qualified Naturopath:</b><br>
                  Your first visit may take more than an hour involving a thorough interview in relation to your diet, lifestyle, stress, genetics, medical history and exposure to environmental substances. An appropriate physical examination may be performed, which may include conventional tests, e.g. Zinc taste testing. This first consultation will give me the whole picture of your condition and the most appropriate and personalized treatment can be prescribed.<br>
